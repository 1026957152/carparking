package com.coalvalue.idCard;

/// <summary>
    /// 读卡相关类
    /// </summary>
    public class CardInfo
    {
        /// <summary>
        /// 身份证的信息
        /// </summary>
        /// <summary>
        /// 身份证号码
        /// </summary>
        public String CardNo;
        /// <summary>
        /// 姓名
        /// </summary>
        public String Name;
        /// <summary>
        /// 性别
        /// </summary>
        public String Sex;
        /// <summary>
        /// 出生日期
        /// </summary>
        public String Birthday;
        /// <summary>
        /// 地址
        /// </summary>
        public String Address;
        /// <summary>
        /// 追加地址
        /// </summary>
        public String AddressEx;
        /// <summary>
        /// 发卡机关
        /// </summary>
        public String Department;
        /// <summary>
        /// 证件开始日期
        /// </summary>
        public String StartDate;
        /// <summary>
        /// 证件结束日期
        /// </summary>
        public String EndDate;
        /// <summary>
        /// 民族
        /// </summary>
        public String Nation;
        /// <summary>
        /// 相片路径
        /// </summary>
        public String PhotoPath;
    }
